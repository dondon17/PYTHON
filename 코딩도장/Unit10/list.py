# list(배열) 만들기
a = [34, 52, 74, 10, 4]
print(a)

# 여러 자료형을 하나의 배열로 지정 가능
b = ['Python', 15, 3.14, True]
print(b)

# 빈 배열
c = []
print(c)
c = list()
print(c)

# range활용
print(range(10))
d = list(range(10))
print(d)
d = list(range(5, 15))
print(d)
d = list(range(1, 100, 10))
print(d)
d = list(range(10, 0, -1))
print(d)