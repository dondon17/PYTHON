# 값을 여러개 출력
# print(값1, 값2, 값3, ...) 또는 print(변수1, 변수2, 변수3, ...)
print(1, 2, 3)

# sep옵션으로 값 사이에 특정 문자 넣기
# print(값1, 값2, 값3, sep = '문자 또는 문자열')
# print(변수1, 변수2, 변수3, sep = '문자 또는 문자열')
print(1, 2, 3, sep = ', ')
print(1920, 1080, sep = 'x')

# 개행 문자 사용
print(1, 2, 3, sep = '\n')

# end 사용
print(4, end='\n')
print(5, end='\n')
print(6, end='\n')
